def build_tree(data, features):
    if not data:
        return None
    if len(set(row["nom_animal"] for row in data)) == 1:
        return data[0]["nom_animal"]
    if not features:
        return max(set(row["nom_animal"] for row in data), key=lambda x: [row["nom_animal"] for row in data].count(x))

    best_feature, best_value = find_best_split(data, features)
    left_data, right_data = split_data(data, best_feature, best_value)
    remaining_features = [f for f in features if f != best_feature]

    left_subtree = build_tree(left_data, remaining_features)
    right_subtree = build_tree(right_data, remaining_features)

    return (best_feature, best_value, left_subtree, right_subtree)
