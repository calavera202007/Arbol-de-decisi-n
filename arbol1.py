def calculate_gini(data):
    total_samples = len(data)
    if total_samples == 0:
        return 0

    gini = 1
    class_counts = {}
    for row in data:
        label = row["nom_animal"]
        class_counts[label] = class_counts.get(label, 0) + 1

    for label in class_counts:
        probability = class_counts[label] / total_samples
        gini -= probability ** 2

    return gini
#aqui dividimos lo sa datos
def split_data(data, feature, value):
    left = [row for row in data if row[feature] == value]
    right = [row for row in data if row[feature] != value]
    return left, right
#aqui me toma la mejor division para hacer el arbol y predecir la proxima opcion
def find_best_split(data, features):
    best_gini = 1
    best_feature = None
    best_value = None

    for feature in features:
        values = set(row[feature] for row in data)
        for value in values:
            left, right = split_data(data, feature, value)
            gini = (len(left) / len(data)) * calculate_gini(left) + (len(right) / len(data)) * calculate_gini(right)
            if gini < best_gini:
                best_gini = gini
                best_feature = feature
                best_value = value

    print("Mejor Split:")
    print("Característica:", best_feature)
    print("Valor:", best_value)
    print("Gini:", best_gini)

    return best_feature, best_value
