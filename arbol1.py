def clase_mas_comun(data):
    clas = [fila["nom_animal"] for fila in data]
    return max(set(clas), key=clas.count)

def calcular_gini(data):
    if not data:
        return 0.0

    clas = [fila["nom_animal"] for fila in data]
    tot = len(clas)
    impu = 1.0

    for clase in set(clas):
        prob = clas.count(clase) / tot
        impu -= prob ** 2
    return impu

