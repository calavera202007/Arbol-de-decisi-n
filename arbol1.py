def construir_arbol(data):
    if not data:
        return clase_mas_comun(data)

    clases = set(fila["nom_animal"] for fila in data)


    if len(clases) == 1:
        return clases.pop()


    if len(data[0]) == 1:
        return clase_mas_comun(data)


    mejor_gini = 1.0
    mejor_division = None

    for atributo in data[0]:
        if atributo != "nom_animal":
            valores = set(fila[atributo] for fila in data)
            for valor in valores:
                conjunto_izquierda = [fila for fila in data if fila[atributo] == valor]
                conjunto_derecha = [fila for fila in data if fila[atributo] != valor]

                gini_izquierda = calcular_gini(conjunto_izquierda)
                gini_derecha = calcular_gini(conjunto_derecha)

                gini_total = (len(conjunto_izquierda) / len(data)) * gini_izquierda + (len(conjunto_derecha) / len(data)) * gini_derecha

                if gini_total < mejor_gini:
                    mejor_gini = gini_total
                    mejor_division = (atributo, valor, conjunto_izquierda, conjunto_derecha)

    if mejor_gini == 1.0:
        return clase_mas_comun(data)
    atributo, valor, conjunto_izquierda, conjunto_derecha = mejor_division
    subarbol_izquierdo = construir_arbol(conjunto_izquierda)
    subarbol_derecho = construir_arbol(conjunto_derecha)

    return {"atributo": atributo, "valor": valor, "izquierdo": subarbol_izquierdo, "derecho": subarbol_derecho}

